                let iTag = document.createElement('i');
                iTag.classList.add("fa", "fa-angles-right", "text-green");
                spanTag.append(iTag);

                aTag.appendChild(spanTag);

                let spanTitleTag = document.createElement('span');
                spanTitleTag.classList.add("m-reg", "text-green", "footer_nav");
                spanTitleTag.innerHTML = element.title;

                aTag.append(spanTitleTag);

                li.appendChild(aTag);

                document.querySelector('#footer-pages-links').appendChild(li);
            
            });
